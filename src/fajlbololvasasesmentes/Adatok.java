    public int legidosebb(String neme) {
        
        int max = emberosztaly.get(0).getKor();
        for (int i = 0; i < emberosztaly.size(); i++) {
            if (emberosztaly.get(i).getNem().equals(neme)) {
                if (emberosztaly.get(i).getKor() > max) {
                    max = emberosztaly.get(i).getKor();
                }
            }
        }
        return max;
    }
     
    public int evekOsszegzes(String neme){
        int ev=0;
        
        for (int i = 0; i <emberosztaly.size() ; i++) {
            if(emberosztaly.get(i).getNem().equals(neme)){
                ev+=emberosztaly.get(i).getKor();
                
            }
                    
                        
        }
        return ev;
        
         
    }
    public String keresesKivalaszt(String neme){
        String nev="";
            for (int i = 0; i < emberosztaly.size(); i++) {
            if(emberosztaly.get(i).getNem().equals(neme)){
            if(emberosztaly.get(i).getMnkaToltottEvek()>6){
                nev+=emberosztaly.get(i).getNev();
            }
            }
            
        }
        return nev;
        
 
    }
    
