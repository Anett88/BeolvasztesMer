    public Adatok() {
        initComponents();
        jFileChooser1.showOpenDialog(jPanel1);
        jFileChooser1.getSelectedFile();
        jFileChooser1.getCurrentDirectory();
        jTextField1.setText(jFileChooser1.getSelectedFile().toString());
        jTextField2.setText(jFileChooser1.getCurrentDirectory().toString());
        try {
            List<String> lista = Files.readAllLines(Paths.get(jFileChooser1.getSelectedFile().toString()));
            emberosztaly = new ArrayList<>();
            for (int i = 0; i < lista.size(); i++) {
                String egySor = lista.get(i);
                String[] adatok = egySor.split(";");
                if (!adatok[0].equals("NÉV")) {
                    if (adatok.length == 3) {
                        Ember e = new Ember(adatok[0], Integer.parseInt(adatok[1]), adatok[2]);
                        emberosztaly.add(e);
                    } else {
                        Ember e = new Ember(adatok[0], Integer.parseInt(adatok[1]), adatok[2], Integer.parseInt(adatok[3]));
                        emberosztaly.add(e);
                    }
                }

            }

            for (int i = 0; i < emberosztaly.size(); i++) {
                if (emberosztaly.get(i).getNem().equals("F")) {
                    jCBFiuk.addItem(emberosztaly.get(i).getNev());
                } else {
                    jCBLanyok.addItem(emberosztaly.get(i).getNev());
                }
            }

        } catch (IOException ex) {
            Logger.getLogger(Adatok.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

